#include <libft.h>

long int	ft_atoli(const char *str)
{
	long int		n;
	long int		x;

	n = 0;
	x = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
			x = -1;
	}
	while (*str >= '0' && *str <= '9')
		n = (n * 10) + (*str++ - '0');
	return (x * n);
}
